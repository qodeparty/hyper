/**
 * Merge the prop of two objects left (Any Prop of {b} overwrites {a})
 *
 * @param  {Object} a
 * @param  {Object} b
 * @return {Object} 
 * @private
 */
  function mixin( a, b ){

    if( a && b ){
      for( let key in b ){
        a[ key ] = b[ key ];
      }
    }
    return a;

  };

/*/////////////////////////////////////////////////////////////////////////////
// Export
/////////////////////////////////////////////////////////////////////////////*/


  module.exports = mixin;